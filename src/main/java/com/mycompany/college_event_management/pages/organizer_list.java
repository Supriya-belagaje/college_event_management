    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = orgtab.getSelectedRow();

// Check if any row is selected
if (selectedRowIndex != -1) {
    // Assuming the organizer ID is stored in the first column of the table
    int organizerIdToDelete = (int) orgtab.getValueAt(selectedRowIndex, 0);
    
    // Ask for confirmation before deleting the organizer
    int confirmDialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this organizer?", "Confirmation", JOptionPane.YES_NO_OPTION);
    
    // Check user's choice
    if (confirmDialogResult == JOptionPane.YES_OPTION) {
        // Attempt to delete the organizer
        boolean deletionSuccessful = Organizer.deleteOrganizer(organizerIdToDelete);

        // Display appropriate message based on deletion result
        if (deletionSuccessful) {
            JOptionPane.showMessageDialog(null, "Organizer deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            // Optionally, refresh the table to reflect the changes
        } else {
            JOptionPane.showMessageDialog(null, "Failed to delete organizer. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
} else {
    // If no row is selected, display an error message
    JOptionPane.showMessageDialog(null, "Please select an organizer to delete.", "Error", JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_jButton3ActionPerformed

