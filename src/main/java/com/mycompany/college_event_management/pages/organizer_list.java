   private void fetchorg(){
        var allorgs = Organizer.getAllOrganizers();
        System.err.println("allEvents" + allorgs.size());
        //Table
        OrganizerTableModel model = new OrganizerTableModel(allorgs);
        orgtab.setModel(model);
        
        //Pannel with delete
        for (Organizer e : allorgs) {
          items.add(e.organizerName);
        }
        refreshItemList();
    }
private void refreshItemList() {
        orglist.removeAll();

        for (String item : items) {
            JPanel itemPanel = new JPanel(new FlowLayout(FlowLayout.LEFT));
            JLabel itemLabel = new JLabel(item);
            JButton deleteButton = new JButton("Delete");
            deleteButton.addActionListener((ActionEvent e) -> {
                items.remove(item);
                refreshItemList();
                // Revalidate and repaint to ensure UI updates are displayed
                orglist.revalidate();
                orglist.repaint();
            });

            itemPanel.add(itemLabel);
            itemPanel.add(deleteButton);
            orglist.add(itemPanel);
            System.out.println("added "+ item);
        }

        // In case the list is empty, show a message
        if (items.isEmpty()) {
            orglist.add(new JLabel("No items available."));
        }
    }
